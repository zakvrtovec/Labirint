let currentIndex = 0;
const circle = {
    x: points[0][0],
    y: points[0][1],
    radius: 5
};

// Flag to control when solving starts
let solving = false;

// Function to update the circle's position
function moveCircle() {
    if (currentIndex < points.length - 1) {
        const target = points[currentIndex + 1];
        ctx.clearRect(0, 0, canvas.width, canvas.height);
        narisi();
        const dx = target[0] - circle.x;
        const dy = target[1] - circle.y;
        const distance = Math.sqrt(dx * dx + dy * dy);

        // Move the circle towards the next point
        if (distance > 1) {
            circle.x += dx / distance * scaledValue;  // Adjust speed here (move by 2 pixels per frame)
            circle.y += dy / distance * scaledValue;
        } else {
            // Once the circle reaches the target point, move to the next point
            currentIndex++;
        }
        if (currentIndex === points.length - 1) {
            playSound();
            solving=false;
        }
    }
}

// Function to draw the circle
function drawCircle() {
    // Clear the canvas
    ctx.beginPath();
    ctx.arc(circle.x, circle.y, circle.radius, 0, Math.PI * 2);
    ctx.fillStyle = 'black';
    ctx.fill();
    ctx.closePath();
}

// Main animation loop
function animate() {
    if (solving) {
        moveCircle();
        drawCircle();
    }
    requestAnimationFrame(animate);  // Keep calling animate
}

